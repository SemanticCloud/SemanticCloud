package org.semanticclud.agency.configuration;

public class ProviderConfiguration {
    private String name;
    private String lib;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLib() {
        return lib;
    }

    public void setLib(String lib) {
        this.lib = lib;
    }
}
